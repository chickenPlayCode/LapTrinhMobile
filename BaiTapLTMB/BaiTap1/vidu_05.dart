void main() {
  // Biến final
  final name = 'Hoang';
  final int age;
  
  age = 19;

  // Hằng số const
  const color = 'red';
  const int material = 1;

  // In ra màn hình để tránh lỗi biến không sử dụng
  print('Name: $name');
  print('Age: $age');
  print('Color: $color');
  print('Material: $material');
}
