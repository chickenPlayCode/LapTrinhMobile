/**
 * Câu 1: Viết một đoạn mã Dart để khai báo các kiểu dữ liệu khác nhau như số nguyên, số thực, chuỗi ký tự và Boolean.
 *  Sau đó, in giá trị của các biến này ra console để xem kết quả.  

Câu 2: Viết một hàm trong Dart tên là calculateSum nhận vào hai tham số số nguyên và trả về tổng của chúng.
 Gọi hàm này trong chương trình chính và in kết quả ra màn hình.
 */
void main() {
  // Khai báo số nguyên (int)
  int soNguyen = 10;
  
  // Khai báo số thực (double)
  double soThuc = 3.14;
  
  // Khai báo chuỗi ký tự (String)
  String chuoiKyTu = "Xin chào, Dart!";
  
  // Khai báo Boolean (bool)
  bool laDung = true;
  
  // In giá trị của các biến ra console
  print("Giá trị số nguyên: $soNguyen");
  print("Giá trị số thực: $soThuc");
  print("Giá trị chuỗi ký tự: $chuoiKyTu");
  print("Giá trị Boolean: $laDung");
}